public class Default_Constructor_Implementation{
    int a;
    double b;
    float c;
    boolean d;
    String e;
    public static void main(String[] args) {
        Default_Constructor_Implementation obj=new Default_Constructor_Implementation();
        System.out.println("Default constructor called");
        System.out.println("a = "+obj.a+"\nb = "+obj.b+"\nc = "+obj.c+"\nd = "+obj.d+"\ne = "+obj.e);
    }
}
